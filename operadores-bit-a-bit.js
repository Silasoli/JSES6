//OPERADORES BIT A BIT

var a = 2;//bit seria 10
var b = 3;//bit seria 11

console.log(a & b);
console.log(a | b);
console.log(a ^ b);
console.log(~b);
console.log(~a);
console.log(a << b);// 10(a) + 3(b) 000 = 10000 (binario) indo pra base decimal =  16
console.log(a >> b);//10(a) + 3(b)000 = 000 (binario) = 0




